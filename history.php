    <div class="container">
         The Suburban Amateur Repeater Association was started in Chicago in 1967.</p>

        <p>We started at two locations; Christ Community Hospital on 95th St in Oak Lawn, IL was our transmit site and Little Company of Mary Hospital in Evergreen Park, IL was our receive site.</p>

        <p>We moved on top of the First National Bank Building at the southwest corner of Clark and Lake Streets in downtown Chicago with an additional receive site at Hollywood Towers on the North Side of Chicago just blocks from Lake Michigan.</p>

        <p>As we continue to seek additional repeater sites in the south, west and north suburban communities, these future improvements will make SARA a formidable communications tool for our Amateur Radio Community.</p>

        <p>Our current location is in the Lincoln Park Community of  Chicago with views of Lake Michigan, downtown Chicago and the North Shore Suburban Community.</p>

        <p>SARA repeater priority: Maintain uptime (availability/reliability) as high as possible.</p>

        <p>We are a proud supporter of local emergency services, the Chicago Marathon, Salvation Army and supplemental services for our local Amateur Radio Clubs and Licensed Hams.</p>
